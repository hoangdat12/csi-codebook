function [W1, W2] = calculateMMSEPrecoding(H1, H2, SNR_dB)
    % H1, H2: [nLayers x nPorts]
    nLayers1   = size(H1, 1);
    nLayers2   = size(H2, 1);
    nPorts     = size(H1, 2);
    
    H          = [H1; H2];                    % [K_total x nPorts]
    noisePower = 10^(-SNR_dB / 10);
    alpha      = noisePower;                   % regularization
    
    % ✅ Công thức MMSE precoder chuẩn: W = H'*(H*H' + α*I)^{-1}
    % W shape: [nPorts x K_total]
    W_MMSE = H' / (H * H' + alpha * eye(nLayers1 + nLayers2));
    
    % ✅ Normalize riêng từng UE để giữ power constraint
    W1_raw = W_MMSE(:, 1:nLayers1);           % [nPorts x nLayers1]
    W2_raw = W_MMSE(:, nLayers1+1:end);        % [nPorts x nLayers2]
    
    W1 = W1_raw / norm(W1_raw, 'fro') * sqrt(nLayers1);
    W2 = W2_raw / norm(W2_raw, 'fro') * sqrt(nLayers2);
    
    % W1, W2 trả về shape [nPorts x nLayers]
    % Trong muMIMO2UE: portFlat = layerFlat * W.' → cần W.' = [nLayers x nPorts]
    % → W shape [nPorts x nLayers] là đúng ✅
end